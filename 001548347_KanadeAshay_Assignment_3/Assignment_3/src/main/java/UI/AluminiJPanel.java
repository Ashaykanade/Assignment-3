private void PopulateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblAdata.getModel();
        model.setRowCount(0);
        
        for (Alumini vs : history.getHistory()){
            
            Object[] row = new Object[7];
            row[0] = vs;
            row[1] = vs.getAID();
            row[2] = vs.getACourse();
            row[3] = vs.getGrade();
            row[4] = vs.getContactNum();
            row[5] = vs.getJob();
            row[6] = vs.getJobPosition();
            
            
            model.addRow(row);
                    
        
    }
        
}
