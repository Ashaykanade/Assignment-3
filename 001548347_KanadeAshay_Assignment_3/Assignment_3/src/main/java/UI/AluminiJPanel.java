    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblAdata.getSelectedRow();
        
        if (selectedRowIndex<0){
            
            JOptionPane.showMessageDialog(this, "Please Select Row to Delete.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblAdata.getModel();
        Alumini Selected =(Alumini)model.getValueAt(selectedRowIndex, 0);
        
        history.deleteVitals(Selected);
        
        JOptionPane.showMessageDialog(this, "Alumini History Deleted.");
        
        PopulateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String Aname = txtAName.getText();
        String AID = txtAID.getText();
        String Course = txtCourse.getText();
        String Grade = txtGrade.getText();
        String ContactNum = txtContactNum.getText();
        String job = txtJob.getText();
        String jobpos = txtJob1.getText();

        Alumini vs = history.addNewAlumini();
        vs.setAID(AID);
        vs.setAName(Aname);
        vs.setACourse(Course);
        vs.setGrade(Grade);
        vs.setContactNum(ContactNum);
        vs.setJob(job);
        vs.setJobPosition(jobpos);

        JOptionPane.showMessageDialog(this,"New Alumini Added.");
        
        txtAID.setText("");
        txtAName.setText("");
        txtCourse.setText("");
        txtGrade.setText("");
        txtContactNum.setText("");
        txtJob.setText("");
        txtJob1.setText("");
       
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblAdata.getSelectedRow();
        
        if (selectedRowIndex<0){
            
            JOptionPane.showMessageDialog(this, "Please Select Row.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblAdata.getModel();
        Alumini SelectedVital =(Alumini)model.getValueAt(selectedRowIndex, 0);
        txtAID.setText(String.valueOf(SelectedVital.getAID()));
        txtAName.setText(String.valueOf(SelectedVital.getAName()));
        txtCourse.setText(String.valueOf(SelectedVital.getACourse()));
        txtGrade.setText(String.valueOf(SelectedVital.getGrade()));
        txtContactNum.setText(String.valueOf(SelectedVital.getContactNum()));
        txtJob.setText(String.valueOf(SelectedVital.getJob()));
        txtJob1.setText(String.valueOf(SelectedVital.getJobPosition()));
        
    }//GEN-LAST:event_btnViewActionPerformed

